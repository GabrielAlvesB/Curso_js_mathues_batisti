function autoEscola (idade){
    if(idade >= 18){
        console.log(`Parabens você tem ${idade} anos, pode se matricular na auto escola`)
    }
    else{
        console.log(`Você ainda não tem idade para se matricular na auto escola desculpe`)
    }
}

autoEscola(19)
autoEscola(9)
autoEscola(18)
autoEscola(17)
